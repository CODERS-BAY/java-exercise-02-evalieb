public class Calculating {
	
	public static void main (String[] args) {
		
		byte a = 4;
		byte b = 7; 
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(b%a);
		
		float c = 4.3f;
		float d = 7; 
		
		System.out.println(c+d);
		System.out.println(c-d);
		System.out.println(c*d);
		System.out.println(c/d);
		System.out.println(c%d);
		System.out.println(d%c);
		
		String cow = "yellow";
		String monkey = "brown";
		System.out.println(cow==monkey);
		System.out.println(cow!=monkey);
		
		int e = 8;
		int f = 9; 
		
		System.out.println(e<f);
		System.out.println(e>f);
		System.out.println(e>=f);
		System.out.println(e<=f);
		
		
		
	
	}
	
}
